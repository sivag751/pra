<h4> Peer Review Policy </h4>
<p> All research articles, and most other article types, published in <b>Asteroid Publishers</b> undergo thorough peer review. This usually involves review by two independent peer reviewers. Individual journals may differ in their peer review processes for example, some journals operate an open and others a closed peer review system. </p>
<p> All submissions to <b>Asteroid Publishers</b> are assessed by an Editor, who will decide whether they are suitable for peer review. Where an Editor is on the author list or has any other competing interest regarding a specific manuscript, another member of the Editorial Board will be assigned to assume responsibility for overseeing peer review. Submissions felt to be suitable for consideration will be sent for peer review by appropriate independent experts identified by the Handling Editor. Editors will make a decision based on the reviewers reports and authors are sent these reports along with the editorial decision on their manuscript. Authors should note that even in light of one positive report, concerns raised by another reviewer may fundamentally undermine the study and result in the manuscript being rejected.</p>
<h4>Peer reviewers</h4>
<p> Authors may suggest potential reviewers if they wish; however, whether or not to consider these reviewers is at the Editor's discretion. Authors should not suggest recent collaborators or colleagues who work in the same institution as themselves. Authors may request exclusion of individuals as peer reviewers, but they should explain the reasons in their cover letter on submission. Please note that the Editor may choose to invite excluded peer reviewers.</p>
