<h4>Manuscript Type</h4>
<p>Firstly, mention the manuscript type like: Research/Review/Case Report/Editorial, etc.</p>
<h4>Manuscript Title</h4>
<p>Manuscript title limit should not exceed 30 words. It should not contain abbreviations and the title should be a brief phrase which describes the contents present in the article.</p>
<h4>Author Details</h4>
<p>The details (full names and affiliations) of all the authors should be given in the manuscript.<br/>
Corresponding author details (Name, designation, Department, university, Live address with Telephone, Fax and Email address) should be provided in the manuscript.</p>
<h4>Instructions to be Followed</h4>
<ul class="ullist">
<li> A brief title describing, content of the image.</li>
<li> Images must be submitted in TIFF, PDF or PPT.</li>
<li> The description regarding the image must be limited (250 words).</li>
<li> Images should be captured at the following minimum resolutions: halftones and color (all color must be RGB) 100-200 dpi; combination halftones 350 dpi; line art 500 dpi to match the journal criteria.</li>
<li> Citations/References (URL) should be mentioned properly.</li>
</ul>
<h4>Presentation</h4>
<p>Presentation is a means of communication that helps a person to disclose his work under a broad spectrum of clinical and medical sciences. Presentations are of three types: Power Point Presentation, Flow Chart and Poster presentation.</p>
