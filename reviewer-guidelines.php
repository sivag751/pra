					   <h3 title="Reviewer Guidelines"> Reviewer Guideline </h3>
<p> The peer-review process and the recommendations of expert reviewers assists the designated member of the Editorial Board to recommend publication of the articles that meet certain standards. Among these is to determine if the: empirical research is of general interest and appropriate to the scope of the journal, uses acceptable methodology, analyses, conclusions are supported by the results and if the submission contributes to or advances the field of study. The submitted articles must be unbiased, well referenced and in the format required of the journal.</p>
<p> Reviewers can recommend to the Editorial office that a submission is accepted “without changes”, accept with recommended “minor revisions”, “major revisions” or “reject”.</p>
<p> Invited members are play a key role in Open Access publishing work and the Peer Review system is an essential procedure for disseminating quality research.</p>
<p> Asteroid Journals Open Access Journals welcomes doctoral level scientists, researchers, academicians and scholars interested in serving as volunteer reviewer. If interested, contact us at <a href="mailto:contact@asteroidpublishers.com">contact@asteroidpublishers.com</a></p>
<h4> Guidelines for Reviewers:</h4>
<ul class="ullist">
<li> Check the novelty of original articles.</li>
<li> Reviewer should not show any favoritism or partiality or prejudice while reviewing the manuscript.  </li>
<li> Reviewers should present the receiving letter once it is assigned.</li>
<li> Reviewers complete with the review process within timeline described in the invitation to review.</li>
<li> Scientific notations, equations, superscript, subscript, figure, diagrams, references, bibliography etc. are devoid of any errors.</li>
<li> If the manuscript does not meet the criteria of the journal or is does not meet scientific rigor, the reviewer conveys this directly inform to the Editorial office.</li>
<li> Articles are assigned based on the research interests of the reviewer. If in case, if the manuscript is beyond the knowledge of reviewer, reviewers should immediately contact the editorial office.</li>
<li> Thoroughly go through the paper and indicate if the paper requires any English grammar, punctuation or spelling to be revised.</li>
<li> Reviewers should provide constructive suggestions and guidelines to authors to make revisions to improve the manuscript, if possible.</li>
</ul>
<h4> Benefits for Peer Reviewers</h4>
<ul class="ullist">
<li> Reviewers who participate in 5 peer-reviews per year will receive a discount on their own future submissions to the journal.</li>
<li> Reviewers who refer colleagues to submit manuscripts to the journal will receive a discount on his/her submission.</li>
</ul>
<h4> Call for Reviewers</h4>
<p> If you would like to begin your association with the journal as a peer reviewer, kindly send the Updated CV, Biography and research interest to: <a href="mailto:contact@asteroidpublishers.com">contact@asteroidpublishers.com</p>
