					   <h3 title="Authors Guidelines"> Authors Guidelines </h3>
<p>The Author Guidelines Regarding Submission of Manuscript Includes:</p>
<h4> 1.	Title/Heading</h4>
<p> Suitable to the subject and not irrelevant to its detail work.</p>
<h4> 2.	Author Affiliation</h4>
<p> Respective Authors Full Names, Department/Institution/University, Country and Contact Number should be drafted in the article which is mandatory.</p>
<h4> 3.	Corresponding Author</h4>
<p> Full Name along with Department/Institution/University, City, State, Country, Telephone, Fax and Email should be included in the manuscript.</p>
<h4> 4.	Abstract</h4>
<p> Should be brief in nature, comprehensive and relevant which is self-explanatory and also relating to the total subject given as summary to the entire script which was narrated by the author while getting the experiment findings in an exhaustive manner. It should also relate to significant outcome and further scope to continue in the field.</p>
<h4> 5.	Keywords</h4>
<p> Should be in alphabetical order separated with semicolons for quick understanding with respect to the subject.</p>
<h4> 6.	Text includes the following</h4>
<ul class="ullist">
<li> Introduction: States the reason for the research: identifies and elaborate discussions in findings of others, includes a review of the literature.</li>
<li> Materials and Methods: Describes the study sample, research population, etc. which completely describes the methods that have been used to gather the information or data collection and describing the means that has been used for measuring or measures.</li>
<li> Results: Summarizes the findings that has been done during the research by using text, charts, graphs and tables along with their legends which is a mandate thing that has to be included in the manuscript.</li>
<li> Discussion: Analysing the findings, explaining the significance or the gist of the research and thereby suggesting the future projects.</li>
<li> Conclusion: The final summary of the research/review work that has been done or that has been seen during the process which should be included.</li>
<li> References: An alphabetical listing of works that were cited and used in supporting the research which may include books, monographs, etc.</li>
</ul>
<h4> 7.	Acknowledgement</h4>
<p> Includes affirmation of individuals, grant details, funds, and conflict of interest. </p>
<p> Submission of manuscript format preferably in Microsoft word. Also, PDF format will be encouraged.</p>
<h4> 8. Processing fee </h4>
<p> Asteroid Publishers is an online international open access, peer reviewed journals publishing organization; we do not have funding/support from any Private Organizations/Government to run. To run our organization in an organized way, we charge manuscript processing fee from the authors to publish their manuscripts. We charge fee to maintain published manuscripts in online repositories. All our articles are published in most updated formats (HTML, PDF, etc.,) </p>
<h4> Manuscript Withdrawal Policy</h4>
<p>Author may wish to withdraw his manuscript after submitting it to Asteroid Publishers.</p>

<p>If an author withdraws manuscript after acceptance of the article, then he/she will be asked to pay 50% of the invoice amount. Each and every manuscript submission costs us (Asteroid Publishers) certain amount along with overhead cost during processing. Manuscripts peer review process is completed (which can often happen within a week), and it has been worked into a journal issue, we have incurred costs on our end that need to be reimbursed.</p>

